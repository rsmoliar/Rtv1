#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[j] != '\0' && len > 0)
	{
		s1[i + j] = s2[j];
		j++;
		len--;
	}
	s1[i + j] = '\0';
	return (s1);
}
